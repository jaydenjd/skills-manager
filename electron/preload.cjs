const { contextBridge, ipcRenderer } = require("electron");

contextBridge.exposeInMainWorld("skillStudio", {
  scan: () => ipcRenderer.invoke("skills:scan"),
  scanUninstalled: () => ipcRenderer.invoke("skills:uninstalled"),
  deleteUninstalledRecords: (dirs) => ipcRenderer.invoke("skills:delete-uninstalled", dirs),
  skillDetail: (payload) => ipcRenderer.invoke("skills:detail", payload),
  diffDirs: (payload) => ipcRenderer.invoke("skills:diff-dirs", payload),
  uninstallSkill: (dir, sourceInfo) => ipcRenderer.invoke("skills:uninstall", dir, sourceInfo),
  installLocalSkill: (dir, targetSourceId, conflictStrategy) => ipcRenderer.invoke("skills:install-local", dir, targetSourceId, conflictStrategy),
  restoreSkill: (dir, targetSourceId, conflictStrategy) => ipcRenderer.invoke("skills:restore", dir, targetSourceId, conflictStrategy),
  reveal: (path) => ipcRenderer.invoke("skills:reveal", path),
  open: (path) => ipcRenderer.invoke("skills:open", path),
  copyText: (text) => ipcRenderer.invoke("clipboard:write", text),
  readFile: (path) => ipcRenderer.invoke("files:read", path),
  createFileEntry: (payload) => ipcRenderer.invoke("files:create", payload),
  renameFileEntry: (payload) => ipcRenderer.invoke("files:rename", payload),
  deleteFileEntry: (payload) => ipcRenderer.invoke("files:delete", payload),
  saveFile: (path, content, context) => ipcRenderer.invoke("files:save", path, content, context),
  fileHistory: (path) => ipcRenderer.invoke("files:history", path),
  fileVersion: (path, versionId) => ipcRenderer.invoke("files:version", path, versionId),
  restoreFileVersion: (path, versionId) => ipcRenderer.invoke("files:restore", path, versionId),
  activateSkillVersion: (payload) => ipcRenderer.invoke("skill-versions:activate", payload),
  deleteSkillVersions: (payload) => ipcRenderer.invoke("skill-versions:delete", payload),
  skillVersionDiff: (payload) => ipcRenderer.invoke("skill-versions:diff", payload),
  previewSkillUpgrade: (payload) => ipcRenderer.invoke("skill-versions:preview-upgrade", payload),
  commitSkillUpgrade: (payload) => ipcRenderer.invoke("skill-versions:commit-upgrade", payload),
  githubTrends: (source, page, query) => ipcRenderer.invoke("github:trends", source, page, query),
  discoverDetail: (item) => ipcRenderer.invoke("discover:detail", item),
  installDiscover: (item, targetSourceId, forceUpdate, conflictStrategy) => ipcRenderer.invoke("discover:install", item, targetSourceId, forceUpdate, conflictStrategy),
  getSources: () => ipcRenderer.invoke("sources:get"),
  saveSources: (sources) => ipcRenderer.invoke("sources:save", sources),
  getAppInfo: () => ipcRenderer.invoke("app:info"),
  getSettings: () => ipcRenderer.invoke("settings:get"),
  saveSettings: (settings) => ipcRenderer.invoke("settings:save", settings),
  operationLogs: () => ipcRenderer.invoke("operations:list"),
  clearOperationLogs: () => ipcRenderer.invoke("operations:clear"),
  submitEvent: (payload) => ipcRenderer.invoke("events:submit", payload),
  operationEvents: () => ipcRenderer.invoke("events:list"),
  clearOperationEvents: () => ipcRenderer.invoke("events:clear")
});
